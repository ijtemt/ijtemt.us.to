<h3 align="center"><u>Paper Transcript of Paper Titled :</u><br>Cyber Threats experienced across the World, India and Uttarakhand 2010 to 2014</h3>
<p align="center">Cyber Threats experienced across the World, India and Uttarakhand 2010  to 2014 <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br>
  <strong>Subodh Prasad1,  M.C. Joshi2</strong></p>
<p align="center">1 Research Scholar,  Department of Information Technology, D.S.B. Campus, Kumaun University,  Nainital, Uttarakhand, India<br>
  <em>subodhprasad3@email.com</em></p>
<p align="center">2 Professor and Head, Department of Information Technology, D.S.B. Campus,  Kumaun University, Nainital, Uttarakhand, India<br>
  &nbsp; <em>mcjoshi69@gmail.com</em></p>
<p><strong>Abstract:</strong> Cyber threats have been identified and reported around the globe. This is  because the people who are using Internet are not delimiting themselves to use  Internet only in their geographic region. And the cyber criminals are also not  targeting any specific country or region in particular. Moreover these threats  have now become more and more personalized in terms of geography, culture,  language etc. As soon as somebody types a domain name or initiates a search on any  search platform, a series of events occur and the person is shown many relevant  results depending upon the search engine&rsquo;s relevancy criteria. These results do  not show any geographic boundary; hence any end user is unaware that from which  country this information is coming to him or her. Also students extensively  make use of Internet nowadays worldwide in order to gain knowledge, do their  class work or home work and then utilize this information and wisdom they have  gained for doing their class works/ home works/ making projects or applications  etc. <br>
  <strong>Keywords: </strong>Cyber  Threats, Cyber Security, Cyber Threats prevailing in Uttarakhand, Cyber threat  Counter Measure Practices.<strong></strong><br>
  <strong><u>I- Introduction</u></strong><br>
  The potential risks of cyber  threats lie in diverse domains, which can be seen as Cyber Terrorism, Cyber war  and Other Cyber Threats (Lewis, 2002). When we see the world we can see these  three potential threats (Cyber Terrorism, Cyber war and Other Cyber Threats)  that can occur. Cyber war as we can understand is carried out by conjuring up  images, information warriors, unleashing vicious attacks against an  unsuspecting opponent&rsquo;s computer network. On a larger scale as of a Nation or  State it can be seen as a major cruel attack of creating chaos and paralyzing  the defence and/or utility system simultaneously of any country or state. It  becomes a chaotic scenario all of a sudden. <br>
  <strong><u>II- Cyber Threats at a  glance</u></strong><br>
  Assessing the cyber threats has  four elements as a whole (Lewis, 2002) which are of the following kinds:-<br>
  a. We should first of all collect  the cyber-warfare and cyber terrorism in the historical context of attacks  against infrastructure.<br>
  b. We should look at the cyber  attacks which have been near to an environment of routine infrastructure  failure. Such data is already available to work upon which includes data on  power outages, flight delays and communications disruptions. Although these can  be said as routine failures but they can be used to understand and take a  lesson from, the effect of vulnerabilities in a system which can lead to  cyber-warfare and cyber-terrorism.<br>
  c. We need to measure the  dependence of infrastructure of any Country/ State on network of computers.  This type of dependence can measure the dependence of infrastructure on  redundancy already present in such systems.<br>
  d. For the case of cyber-terrorism,  we can make use of cyber-weapons in the real perspective of the political goals  and such motives of terrorists. It must also be kept in minds that these are  cyber-weapons capable enough of achieving these goals?<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; As  the world is witnessing cyber threats and cyber attacks so is India. India consists  of 28 states and 8 Union Territories; each of them has been facing cyber treats  in one way or the other. <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Uttarakhand  is not left untouched with the penetration of Internet. Being a newly formed  state and with the day by day penetration of Internet towards urban and rural  areas in the state, such study of the residents of the state towards being  informed about, various prevailing cyber threats and the practices to counter  measure them, is the need of the hour.<br>
  <strong><u>III- Cyber Threats Witnessed  globally for a period of half a decade</u></strong><br>
  <strong><u>In the Year 2010:</u></strong></p>
<ol>
  <li>More  than 300K complaints of consumers of fraud and scams were received by FBI. [1]</li>
  <li>Kaspersky  Lab had reported more than 500 Million Cyber Attacks Against Users. [2]</li>
</ol>
<p><strong><u>In the Year 2011:</u></strong></p>
<ol>
  <li>In  2011, the Washington Post reported that U.S. intelligence organizations carried  out 231 cyber-operations under a campaign of cyber warfare. [3]</li>
  <li>FBI  received more than 300K complaints of consumers related to frauds and scams. [4]</li>
  <li>Kaspersky  had reported the number of cyber attacks they detected increased to more than  90 Million in 2011. [5]</li>
</ol>
<p><strong><u>In the Year 2012:</u></strong></p>
<ol>
  <li>Department  of Homeland Security of United States (DHS) reported that the cyber attacks on  critical infrastructure have increased in 2012. [6]</li>
  <li>Kaspersky  Labs reported Nearly 1.6 Billion Web-based attacks. [7]</li>
  <li>Botnets  were discovered in nearly 700K infected Apple computers. [8]</li>
</ol>
<p><strong><u>In the Year 2013:</u></strong></p>
