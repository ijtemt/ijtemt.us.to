<p align="center"><strong>The  Role of Open and Distance Learning in Commerce Education: A Study of  Uttarakhand Open University</strong></p>
<p align="center">Dr. Gagan Singh<br>
  Assistant  Professor<br>
  School of  Management Studies &amp; Commerce<br>
  Uttarakhand Open  University<br>
  Haldwani-Nainital,  Uttarakhand-263139</p>
<p align="center"><strong>Abstract</strong><br>
  <em>Distance  learning caters to the educational needs of the target groups through the open  system of learning and provides facility of study according to their own pace  and convenience to the distant learner&rsquo;s. Open and distance learning provides  easy access to education to different sections of the society. </em><em>Commerce education is one of the oldest branches of Indian  higher education system. But nowadays commerce education is competing with new  streams for its existence because these streams are based on the practical  knowledge and application orientation. </em><em>Commerce education is a living  discipline and it is considered to be the backbone of business. The need of the  hour is to make a corresponding change in the role of commerce education so  that it becomes purposive and practical-oriented. Distance learning makes  education open to many people irrespective of age, sex, religion, location,  qualification and time, and it provides opportunities for working people to  combine education and their work. It helps in developing education as a  lifelong activity to enable persons to update their knowledge. In other words,  it provides access to higher education for large segments of the population,  and in particular, the disadvantaged groups like people living in remote and  rural areas.</em><br>
  <em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Distance  learning is an old idea with a new name. Its foundation goes back to the early  1700s when through correspondence students and instructors exchanged  information through the postal system or other modes of communication. This  paper attempts to study the importance of open and distance learning in  imparting commerce education to the distance learners and its significance in  making them independent in the present competitive world. This study also  attempts to outline the opportunities which open and distance learning can  provide to learners in India and especially in Uttarakhand for making them  actively participate in stimulating socio-economic development of the country.  This study aims at examining the role of Uttarakhand Open University in providing  flexible, accessible, qualitative and cost-effective education to the distance  learners so that they can contribute to a more sustainable and prosperous  society.</em><br>
  <strong><em>Key  Words: </em></strong><em>Accessible;  Counselling; Correspondence; Distance Learning; Learners; Technology.<strong></strong></em></p>
<p><strong>Introduction</strong><br>
  Distance  education provides opportunity of learning to those, who do not have direct access  to face to face teaching. It also provides direct access of learning to the working  persons, house&#8208;wives and marginalized section of the  society. Distance education provides opportunity to working professionals to  update their knowledge, enabling them to switch over to new disciplines and  professions and enhancing their qualifications for career advancement. It helps  in exploiting the potential of Information and Communication Technology (ICT)  in the teaching and learning process. Open learning is a philosophy and  distance education is the mode used for translating it into reality. Both of  these are considered complementary to each other. Distance education is an  umbrella term which describes all the teaching learning arrangements in which  the learner and the teacher are separated by space and time. In fact it is a  mode of delivering education and instruction to learners who are not physically  present in a traditional setting of a classroom. Transaction of the curriculum  is effected by means of specially prepared materials (self-study (learning) materials)  which are delivered to the learners at their doorstep through various media  such as print, television, radio, satellite, audio/video tapes, CD-ROMs,  Internet and World Wide Web etc. Communication between the institution, teacher  and learners is mainly through electronic media and also through postal correspondence  and limited face to face contact sessions held at study centres that are set up  by the distance education institutions as close to the learners&rsquo; homes as  possible. The popularity of the open and distance learning is increasing day by  day because of its flexibility and learner friendly approach, particularly to  those who could not get access to the formal education system. <strong>(Fozdar and  Kumar, 2008)</strong><br>
  Open learning, which covers a wide range of innovations  and reforms in the educational sector that advocates flexibility to the learner  with regard to entry and exit; pace and place of study; method of study and  also the choice and combination of courses; assessment and course completion.  The open learning system aims to redress social or educational inequality and  to offer opportunities not provided by conventional colleges or universities. In  the system of open and distance learning, educational opportunities are planned  deliberately so that access to education is available to larger sections of the  society. Distance and open learning may also mean a more learner-centred  approach, allowing greater flexibility and choice of content as well as more  personal organization of the learning programme. (<strong>UNESCO, 2002</strong>)<br>
  <strong>Growth  of Open and Distance Learning System</strong><br>
  Distance  learning is an old idea with a new name. Its foundation goes back to the early  1700s when through correspondence students and instructors exchanged information  (assignments, notes, and tests) through the postal system or other modes of  transportation. <strong>(Harper, Chen&nbsp; &amp;  Yen, 2004) </strong>The growth of distance education has been exponential over the  last four decades in our country. With the establishment of Dr. B. R. Ambedkar  Open University, Hyderabad in 1982, a new chapter in the distance education  system began when full-fledged Open University with the mandate to provide  distance education was established in India. This was followed by the  establishment of Indira Gandhi National Open University in 1985. Five decades  ago policy-makers realized the imperative need of distance education in order  to expand the base of higher education. With the expanding base at the  elementary and secondary education levels, the demand for higher education had  increased. The University Grants Commission (UGC) suggested in its report for  1956-1960 that proposals for evening colleges, correspondence courses and award  of external degrees should be considered. The Planning Commission took serious  note of such a need and in its Third Five Year Plan mentioned the need for the  introduction of correspondence education in the country. <strong>(UGC, DEB)</strong> <strong> </strong><br>
