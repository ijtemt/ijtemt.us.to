<p><strong>Conclusion  and Suggestions</strong><br>
  Commerce  education is one of the oldest branches of Indian higher education system. It  is regarded as the base of many streams of education such as management,  accounting, insurance, banking, taxation, finance, marketing and business law  in India. But nowadays commerce education is competing with these new streams  for its existence, as these branches are following practical based or skill  oriented pedagogy. In other words, it is losing its sheen as compared to the  other practical based disciplines. Moreover, in the present scenario, due to  globalization and advancement of information technology, competitive  marketplace is emerging and education systems are changing. Distance  education is mode to provide an opportunity to those who left the study in  between and want to be part of the regular system and want to be part of the  learned society. The  main objective of distance education is to democratize higher  education to large segments of the population, in particular the disadvantaged  groups such as those living in remote and rural areas, working people, women  etc. It also develops education as a lifelong activity to enable persons to  update their knowledge or acquire knowledge in new areas. Open and distance learning occupies a special place in the  Indian higher education system because of its major contribution in enhancing  the gross enrollment ratio and democratization of higher education to large  segments of the Indian population particularly to reach out to the unreached  and to meet the demands of lifelong learning which has become more of a necessity  in the knowledge society.<br>
  Commerce education is facing multiple  challenges in the present scenario. As we know it is a living discipline and  needs to keep pace with the changes happening locally as well as globally. To  bring back the dignity and glory of commerce education as it had in the past, a  new orientation requires to be given to the discipline of commerce education.</p>
<ul>
  <li>There  is need to update the curriculum of commerce and linked it to the needs of day  to day life of the students or with the needs of the industry. </li>
  <li>There should be more focus on the practical aspects rather than  theoretical aspects. Computerized accounting has been introduced as compulsory  part of the course.</li>
  <li>Commerce curriculum needs revision and it should be design to  develop skills such as communication, decision making, problem solving,  leadership etc. Keeping in view the changes in business  environment, the syllabus needs to be redesigned at regular intervals. </li>
  <li>To update themselves with the ever-changing business  environment, there is need to encourage the faculties of colleges or  universities to participate in various training programs.</li>
  <li>The interaction with the industry and  linkages with the professional bodies and the recruitment agencies should be  encouraged. </li>
</ul>
<p>Therefore, it is the  need of the hour that more emphasis must be given to distance learning so that  the left out and those who enable to carry on their study through regular mode  of education can get a chance of learning through distance mode of education  and be a part of learned society. <br>
