  <strong>Review of Literature</strong><br>
  <strong>Reddy  (2007), </strong>in  his study &ldquo;Revitalizing Commerce Education&rdquo; made an objective introspection about  the Commerce Education &ndash; its objectives, its problems, its job potential, its  quality and its relevance to the present day needs of our country. In this study he  emphasized on the re-designing of commerce education and suggested various  alternatives available for re-orientation of commerce education<strong>.&nbsp; </strong><br>
  <strong>Singh (2014), </strong>conducted a study on &ldquo;Commerce  Education in 21st Century: Prospectus and Challenges. The purpose of  the paper is to understand and review various issues that influencing and  related with commerce education. This paper indicates and concludes that there  is a strong need to refine and define the commerce education in the global era  and in the changing business scenario. <strong>&nbsp;</strong><br>
  <strong>Kumar  and Fozdar, </strong>conducted  a study on &ldquo;The Role Open and Distance Learning in Vocational  Education and Training in India&rdquo;. In this paper they have reviewed the present  status of vocational education and training programmes in India. They have  examined the role of open and distance learning (ODL) system in providing  effective and dynamic vocational education and training in the country. They  also included IGNOU&rsquo;s experience in launching and effectively delivering of a  vocational training programme developed for the work force working for footwear  sector to improve their skills.&nbsp; <br>
  <strong>Pulist,</strong> conducted a study on  &ldquo;Student Support Services in Correspondence/Distance Education in India: A  Historical Perspective&rdquo;. In the study author highlighted the necessity of Open  and Distance Learning in imparting quality higher education through optimum use  of technology. According to the author the student support services eventually  aim at compensating for the absence of live support from teacher for the  benefit of the isolated individual learners and making the necessaries basic  facilities available to them directly or indirectly.&nbsp; <br>
  <strong>Srivastava (2002),</strong> in his study &ldquo;A  Comparative Study on Current Trends in Distance Education in Canada and India&rdquo;  highlighted that the formal system was unable to meet the demand for higher  education in India and distance education was adopted as an alternative mode at  the University stage in 1962. In both Canada and India distance education  opportunities at the university level have increased enormously. Many trends  are putting new pressures on the conventional education system, forcing many  institutions to review and amend their existing policies and procedures.<strong></strong><br>
  <strong>Need and Scope of the  Study</strong><br>
  Distance learning concept introduced in the  country with a view to democratize education, so that it covers large segments  of population, vocations and professions. The primary emphasis is on  innovation, flexibility and cost effectiveness. It aims at the dissemination of  learning and acquiring knowledge through distance education mode including the  use of any communication technology to provide opportunities for higher  education. Open and distance learning mode  of education has been recognized as one of the effective tools of educationally  empowering the sections of the society who could not afford to join the formal  on-campus education due to various reasons.&nbsp;  Presently, Open and Distance Learning institutions are not only  imparting education as an alternative to the formal system but also in areas  such as vocational and technical education and even in high technology based  education. The distance education also has potential to reach to the un-reached  and even marginalized and excluded groups such as tribal populations and women.<br>
  The main focus of the present study is to examine  the role of open and distance learning in imparting commerce education to the  distance learners. The universe of the present study is confined to Uttarakhand  Open University. The area of the study aims at examining the role of UOU in  providing flexible, accessible, qualitative and cost-effective education to the  distance learners, so that they can contribute to a more sustainable and  prosperous society.<strong> </strong><br>
  <strong>Objectives of the  Study</strong><br>
  In view of the above  discussion, the following objectives have been visualized for the present  study:</p>
<ul>
  <li>To study the role of open and distance learning in commerce  education.</li>
  <li>To pin point the challenges before distance mode of  learning.</li>
  <li>To suggest remedial measures for commerce education  through open and distance mode of learning. </li>
</ul>
<p><strong>Research Methodology</strong><br>
  The  present study has been conducted to examine the role of open and distance  learning in imparting commerce education to the distant learners. The data  required for the study is collected from secondary sources. In the light of  above objectives set forth the information has been collected from records of  Govt. of India, Govt. of Uttarakhand and Uttarakhand Open University. The  learner includes both rural and urban. The data drawn  from various sources has been analyzed with the help of mathematical test as  percentage etc.<strong></strong><br>
