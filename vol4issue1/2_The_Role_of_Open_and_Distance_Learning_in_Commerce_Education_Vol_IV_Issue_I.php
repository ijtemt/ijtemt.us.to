<h2 align="justify" ><a href="#">	
Paper Title :<br>The Role of Open and Distance Learning in Commerce Education: A Study of Uttarakhand Open University</a></h2>
<p align="justify"><strong>Abstract</strong><br>
Distance learning caters to the educational needs of the target groups through the open system of learning and provides facility of study according to their own pace and convenience to the distant learner’s. Open and distance learning provides easy access to education to different sections of the society. Commerce education is one of the oldest branches of Indian higher education system. But nowadays commerce education is competing with new streams for its existence because these streams are based on the practical knowledge and application orientation. Commerce education is a living discipline and it is considered to be the backbone of business. The need of the hour is to make a corresponding change in the role of commerce education so that it becomes purposive and practical-oriented. Distance learning makes education open to many people irrespective of age, sex, religion, location, qualification and time, and it provides opportunities for working people to combine education and their work. It helps in developing education as a lifelong activity to enable persons to update their knowledge. In other words, it provides access to higher education for large segments of the population, and in particular, the disadvantaged groups like people living in remote and rural areas.<br><br>
Distance learning is an old idea with a new name. Its foundation goes back to the early 1700s when through correspondence students and instructors exchanged information through the postal system or other modes of communication. This paper attempts to study the importance of open and distance learning in imparting commerce education to the distance learners and its significance in making them independent in the present competitive world. This study also attempts to outline the opportunities which open and distance learning can provide to learners in India and especially in Uttarakhand for making them actively participate in stimulating socio-economic development of the country. This study aims at examining the role of Uttarakhand Open University in providing flexible, accessible, qualitative and cost-effective education to the distance learners so that they can contribute to a more sustainable and prosperous society.
</p>
